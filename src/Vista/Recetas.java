    public void cargarTabla() {
        modelo.setRowCount(0);
        RecetaMedica[] lista = SistemaClinico.listarRecetas();
        for (int i = 0; i < SistemaClinico.cantidadRecetas(); i++) {
            modelo.addRow(new Object[]{
                lista[i].getMedicamento(),
                lista[i].getDosis(),
                lista[i].getDuracion()
            });
        }
    }
    
