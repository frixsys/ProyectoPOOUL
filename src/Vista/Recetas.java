    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        // TODO add your handling code here:
        String med = jtMedicamento.getText();
        String dos = jtDosis.getText();
        String dur = jtDuracion.getText();
        
        if(!med.isEmpty() && !dos.isEmpty()) {
            RecetaMedica nueva = new RecetaMedica(med, dos, dur);
            SistemaClinico.agregarReceta(nueva);
            cargarTabla();
            jtMedicamento.setText(""); jtDosis.setText(""); jtDuracion.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Complete los datos.");
        }
    }//GEN-LAST:event_bAgregarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        // TODO add your handling code here:
        int fila = jtRecetas.getSelectedRow();
        if (fila >= 0) {
            SistemaClinico.eliminarReceta(fila);
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una receta.");
        }
    }//GEN-LAST:event_bEliminarActionPerformed

